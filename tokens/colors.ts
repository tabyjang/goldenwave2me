/**
 * Design Token System - Color Tokens
 *
 * 6개의 색상 패키지를 타입 안전한 토큰으로 정의합니다.
 * Single Source of Truth for all color values.
 */

// 색상 스케일 인터페이스 (50-950)
export interface ColorScale {
  50: string
  100: string
  200: string
  300: string
  400: string
  500: string   // Base color
  600: string
  700: string
  800: string
  900: string
  950: string
}

// 의미론적 색상 (성공, 경고, 에러)
export interface SemanticColors {
  success: ColorScale
  warning: ColorScale
  error: ColorScale
  info: ColorScale
}

// 색상 토큰 구조
export interface ColorToken {
  primary: ColorScale      // 주요 브랜드 색상
  accent: ColorScale       // 강조 색상
  neutral: ColorScale      // 중립 색상 (회색 계열)
  semantic: SemanticColors // 의미론적 색상
}

// 색상 패키지 인터페이스
export interface ColorPackage {
  name: string
  description: string
  colors: ColorToken
}

